#Conditional Statements
def main():
    x = 20
    y = 20
    if x < y:
        print("x < y is true")
    elif x > y:
        print("x > y is true")
    elif x == y:
        print("x = y is true")
    else:
        print("there is no condition for x and y")
if __name__ == '__main__':
    main()